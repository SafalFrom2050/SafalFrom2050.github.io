const https = require('https');
const fs = require('fs');
const path = require('path');

const POSTS_DIR = path.join(__dirname, '../blog/posts');
const PROJECT_ID = 'dif-instantgames';

function slugify(text) {
  return text.toString().toLowerCase()
    .replace(/\s+/g, '-')           // Replace spaces with -
    .replace(/[^\w\-]+/g, '')       // Remove all non-word chars
    .replace(/\-\-+/g, '-')         // Replace multiple - with single -
    .replace(/^-+/, '')             // Trim - from start of text
    .replace(/-+$/, '');            // Trim - from end of text
}

async function fetchAllGames() {
    let allGames = [];
    let nextPageToken = null;
    
    do {
        let url = `https://firestore.googleapis.com/v1/projects/${PROJECT_ID}/databases/(default)/documents/ai_games?pageSize=300`;
        if (nextPageToken) {
            url += `&pageToken=${encodeURIComponent(nextPageToken)}`;
        }

        const data = await new Promise((resolve, reject) => {
            https.get(url, (res) => {
                let body = '';
                res.on('data', chunk => body += chunk);
                res.on('end', () => resolve(JSON.parse(body)));
            }).on('error', reject);
        });

        if (data.documents) {
            allGames = allGames.concat(data.documents);
        }
        
        nextPageToken = data.nextPageToken;
        
    } while (nextPageToken);

    return allGames;
}

async function main() {
    const games = await fetchAllGames();
    
    const publishedGames = games.filter(doc => {
        const fields = doc.fields;
        if (!fields) return false;
        return fields.isPublished?.booleanValue === true && fields.publishStatus?.stringValue === 'complete';
    });

    console.log(`Found ${publishedGames.length} published games.`);

    publishedGames.forEach(doc => {
        const fields = doc.fields;
        const title = fields.title?.stringValue || 'Untitled Game';
        let slug = slugify(title);
        
        // Skip existing ones that we don't want to overwrite blindly
        if (['ai-game-generation', 'neon-beats-review', 'neon-beats-fever-edition', 'alarm-smash-final-hour', 'velocity-vestige-ar-rhythm', 'vitreous-keys-ar'].includes(slug)) {
            console.log(`Skipping ${slug} (already exists or custom).`);
            return;
        }

        const category = fields.category?.stringValue || 'AI Game';
        const description = fields.description?.stringValue || 'An amazing AI-generated game.';
        const prompt = fields.originalPrompt?.stringValue || 'Generated by our powerful Game Developer Agent.';
        const imageUrl = fields.screenshotUrls?.arrayValue?.values?.[0]?.stringValue || '';
        let date = '2026-04-28';
        if (fields.createdAt?.timestampValue) {
            date = fields.createdAt.timestampValue.split('T')[0];
        }
        
        const mdContent = `---
title: "${title}"
date: "${date}"
category: "${category}"
imageUrl: "${imageUrl}"
review: "${description.replace(/"/g, '\\"')}"
---

## Experience ${title}

${description}

### The Prompt That Started It All

Every game on our platform begins with a simple idea. For this game, the creator prompted: 
*"${prompt}"*

Within minutes, our Game Developer Agent turned that vision into a reality, handling all the logic, rendering, and gameplay mechanics automatically. 

### Why You Should Play It
This is a prime example of what's possible when AI empowers creativity. You don't need coding skills or expensive hardware—just your imagination.

<br>
<a href="https://play.google.com/store/apps/details?id=dif.instantgames" style="display:inline-block; padding:12px 24px; background:#22d3ee; color:#0f172a; text-decoration:none; border-radius:15px; font-weight:800; font-family:'Outfit', sans-serif;">Try building your own game in just a minute</a>
`;

        fs.writeFileSync(path.join(POSTS_DIR, `${slug}.md`), mdContent);
        console.log(`Generated ${slug}.md`);
    });
}

main().catch(console.error);
